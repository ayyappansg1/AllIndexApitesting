package com.qa.hcm.api.pojo;

public class Weights {
	 private Object instruments;
	 private int weightId;
	 private String reset_to;
	 private int weighting_type;
	public Object getInstruments() {
		return instruments;
	}
	public void setInstruments(Object instruments) {
		this.instruments = instruments;
	}
	public int getWeightId() {
		return weightId;
	}
	public void setWeightId(int weightId) {
		this.weightId = weightId;
	}
	public String getReset_to() {
		return reset_to;
	}
	public void setReset_to(String reset_to) {
		this.reset_to = reset_to;
	}
	public int getWeighting_type() {
		return weighting_type;
	}
	public void setWeighting_type(int weighting_type) {
		this.weighting_type = weighting_type;
	}
	 
}
